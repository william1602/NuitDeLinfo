        <h1>Bienvenue </h1>
            <h2>Présentation de l'application</h2>
            <p>
                Le surf est un sport qui nous fait réver, plages paradisiaques, eau clair et limpide, tout est mis en oeuvre dans les publicités pour donner envie. Malheureusement, les surfers sont confrontés a une toute autre réalité : maladies recurentes (otites,gasto-enterites,irritation cutanées), polution, eaux remplies de déchets... C'est pourquoi nous avons décider de créer cette application pour aider tous les surfer, à pratiquer ce sport dans de meilleurs conditions d'une part, et d'un autre point de vue diminuer la polution sur les plages.
                </p>
            <h2>Pourquoi utiliser cette application ?</h2>
            <p>
                L'application offrira des informations sur chacun des spots de surf ainsi que des previsions météorologie et de houle mais aussi des commentaires que chacun d'entre vous pourez entrer concernant le lieu. L'application permettra aussi de suivre des cours et tuttos avancés pour les plus ambitieux. Nous voulons offrir la meilleur experience possible pour tous les surfers et ameliorer l'etat des plages tout en faisant découvrir un maximum de sports 
                </p>
            <h2>Qui sommes nous ?</h2>
            <p>
                Surfrider Foundation Europe est une association loi 1901 qui agit depuis 30 ans pour la préservation des océans et de la santé de ses usagers. Nous travaillons sur 3 enjeux : les déchets, la qualité des eaux littorales et le changement climatique. L’association est née à Biarritz en 1990 suite à la mobilisation de surfeurs en colère. Leur terrain de jeu était mis en danger et leur santé menacée. Aujourd’hui, l’ADN et la culture surf transpirent toujours au sein des membres fondateurs de l’organisation. La cause, elle, a dépassé cette « tribu » locale du Sud Ouest de la France pour rassembler des citoyens dans toute l’Europe. Plus d’informations sur : www.surfrider.eu
                </p>

